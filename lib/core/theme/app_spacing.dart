  static const double lg = 24; // 6 units
  static const double xl = 32; // 8 units
  static const double xxl = 48; // 12 units
  static const double xxxl = 64; // 16 units

  // Page padding
  static const double pagePaddingHorizontal = 24;
  static const double pagePaddingVertical = 24;

  // Card padding
  static const double cardPadding = 16;
  static const double cardPaddingLarge = 24;

  // Border radius
  static const double radiusSm = 4;
  static const double radiusMd = 8;
  static const double radiusLg = 12;
  static const double radiusXl = 16;
  static const double radiusFull = 9999;

  // Sidebar
  static const double sidebarWidth = 256;
